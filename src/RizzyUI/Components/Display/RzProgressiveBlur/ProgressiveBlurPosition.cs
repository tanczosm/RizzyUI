namespace RizzyUI;

/// <summary>
/// Represents the vertical placement of the progressive blur overlay.
/// </summary>
public enum ProgressiveBlurPosition
{
    /// <summary>
    /// Renders the blur overlay at the top edge.
    /// </summary>
    Top,

    /// <summary>
    /// Renders the blur overlay at the bottom edge.
    /// </summary>
    Bottom,

    /// <summary>
    /// Renders the blur overlay across the full height so both edges are blurred.
    /// </summary>
    Both
}
