namespace RizzyUI;

/// <summary>
/// Represents the flow direction for <see cref="RzMarquee"/>.
/// </summary>
public enum MarqueeOrientation
{
    /// <summary>
    /// Scrolls content from right-to-left (or left-to-right when reversed).
    /// </summary>
    Horizontal,

    /// <summary>
    /// Scrolls content from bottom-to-top (or top-to-bottom when reversed).
    /// </summary>
    Vertical
}
