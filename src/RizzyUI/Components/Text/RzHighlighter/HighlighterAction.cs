/// Defines the annotation style used by <see cref="RzHighlighter"/>.
/// </summary>
public enum HighlighterAction
{
    /// <summary>
    /// Draws a hand-drawn highlight behind the text.
    /// </summary>
    Highlight,

    /// <summary>
    /// Draws a hand-drawn underline beneath the text.
    /// </summary>
    Underline
}
