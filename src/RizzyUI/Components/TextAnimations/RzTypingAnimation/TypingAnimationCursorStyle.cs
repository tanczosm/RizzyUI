namespace RizzyUI;

/// <summary>
/// Represents the available cursor glyph styles for <see cref="RzTypingAnimation"/>.
/// </summary>
public enum TypingAnimationCursorStyle
{
    /// <summary>
    /// Uses a vertical line cursor.
    /// </summary>
    Line,

    /// <summary>
    /// Uses a block cursor.
    /// </summary>
    Block,

    /// <summary>
    /// Uses an underscore cursor.
    /// </summary>
    Underscore
}
