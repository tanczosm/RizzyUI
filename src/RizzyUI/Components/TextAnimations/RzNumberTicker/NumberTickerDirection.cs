namespace RizzyUI;

/// <summary>
/// Defines the animation direction used by <see cref="RzNumberTicker"/>.
/// </summary>
public enum NumberTickerDirection
{
    /// <summary>
    /// Animates from <c>StartValue</c> toward <c>Value</c>.
    /// </summary>
    Up,

    /// <summary>
    /// Animates from <c>Value</c> toward <c>StartValue</c>.
    /// </summary>
    Down
}
