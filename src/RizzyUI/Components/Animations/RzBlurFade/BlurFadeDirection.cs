namespace RizzyUI;

/// <summary>
/// Defines the initial movement direction for <see cref="RzBlurFade"/> reveal animations.
/// </summary>
public enum BlurFadeDirection
{
    /// <summary>
    /// Reveals content by moving upward from below.
    /// </summary>
    Up,

    /// <summary>
    /// Reveals content by moving downward from above.
    /// </summary>
    Down,

    /// <summary>
    /// Reveals content by moving left from the right.
    /// </summary>
    Left,

    /// <summary>
    /// Reveals content by moving right from the left.
    /// </summary>
    Right
}
