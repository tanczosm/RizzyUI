namespace RizzyUI;

/// <summary>
/// Defines the visual presentation for <see cref="RzFileInput"/>.
/// </summary>
public enum FileInputVariant
{
    /// <summary>
    /// Uses the default compact trigger style.
    /// </summary>
    Default,

    /// <summary>
    /// Uses a compact button-like trigger style.
    /// </summary>
    Button,

    /// <summary>
    /// Uses a larger drag-and-drop dropzone style.
    /// </summary>
    Dropzone
}
